class _Pizza_slice:
  def __init__(self, width, height, x, y):
    self.width = width
    self.height = height
    self.x = x
    self.y = y

    def get_pos():
      return self.x, self.y

    def get_dimensions():
      return self.row, self.col

    def set_x(self, i):
      self.x = i

    def set_y(self, i):
      self.y = i

    def set_width(self, i):
      self.row = i

    def set_height(self, i):
      self.col = i

    def getX():
      return self.x

    def getY():
      return self.y

    def getWidth():
      return self.row

    def getRow():
      return self.col
