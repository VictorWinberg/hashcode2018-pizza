class Pizza_slice():
  def __init__(self, width, height, x, y):
    self.width = width
    self.height = height
    self.x = x
    self.y = y

  def get_pos():
    return self.x, self.y

  def get_dimensions():
    return self.width, self.height

  def set_x(self, i):
    self.x = i

  def set_y(self, i):
    self.y = i

  def set_width(self, i):
    self.width = i

  def set_height(self, i):
    self.height = i

  def getX(self):
    return self.x

  def getY(self):
    return self.y

  def getWidth():
    return self.width

  def getHeight():
    return self.height
